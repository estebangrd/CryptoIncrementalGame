#!/usr/bin/env node
/**
 * One-time script to download 3 months of BTC/USDT 1-minute close prices
 * from Binance public API and write src/data/btcPriceHistory.ts
 *
 * Usage: node scripts/downloadBtcHistory.mjs
 */

import { writeFileSync, appendFileSync, rmSync, existsSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));

const SYMBOL = 'BTCUSDT';
const INTERVAL = '1m';
const LIMIT = 1000; // max per Binance request
const DELAY_MS = 250; // between requests to respect rate limits

// Fixed window: 3 months starting June 1, 2025
const startTime = new Date('2025-06-01T00:00:00.000Z').getTime();
const endTime   = new Date('2025-09-01T00:00:00.000Z').getTime();

const sleep = (ms) => new Promise(resolve => setTimeout(resolve, ms));

async function fetchKlines(start, end) {
  const url = `https://api.binance.com/api/v3/klines?symbol=${SYMBOL}&interval=${INTERVAL}&limit=${LIMIT}&startTime=${start}&endTime=${end}`;
  const res = await fetch(url);
  if (!res.ok) throw new Error(`Binance API error ${res.status}: ${await res.text()}`);
  return res.json();
}

async function main() {
  const prices = [];
  let cursor = startTime;
  let batch = 0;

  const totalMinutes = Math.round((endTime - startTime) / 60000);
  console.log(`Downloading ~${totalMinutes} minutes of BTC/USDT data...`);
  console.log(`From: ${new Date(startTime).toISOString()}`);
  console.log(`To:   ${new Date(endTime).toISOString()}`);
  console.log('');

  while (cursor < endTime) {
    const batchEnd = Math.min(cursor + LIMIT * 60 * 1000, endTime);
    const klines = await fetchKlines(cursor, batchEnd);

    if (klines.length === 0) break;

    // Binance kline format: [openTime, open, high, low, close, ...]
    for (const k of klines) {
      prices.push(parseFloat(k[4])); // close price
    }

    // Advance cursor to after the last candle
    cursor = klines[klines.length - 1][0] + 60 * 1000;
    batch++;

    process.stdout.write(`\rBatch ${batch}: ${prices.length} / ~${totalMinutes} points fetched...`);

    if (cursor < endTime) await sleep(DELAY_MS);
  }

  console.log(`\n\nDone! Total points: ${prices.length}`);
  const minPrice = prices.reduce((a, b) => a < b ? a : b, Infinity);
  const maxPrice = prices.reduce((a, b) => a > b ? a : b, -Infinity);
  console.log(`Min price: $${minPrice.toFixed(2)}`);
  console.log(`Max price: $${maxPrice.toFixed(2)}`);

  const outPath = join(__dirname, '../src/data/btcPriceHistory.ts');
  if (existsSync(outPath)) rmSync(outPath);

  const header = `// Auto-generated by scripts/downloadBtcHistory.mjs — do not edit manually.
// Source: Binance public API — BTCUSDT 1m close prices, 3 months (2025-06-01 to 2025-09-01)
// Generated: ${new Date().toISOString()}
// Total points: ${prices.length}
export const BTC_PRICE_HISTORY: number[] = [\n`;

  writeFileSync(outPath, header, 'utf8');

  // Write in chunks of 5000 to avoid call stack issues
  const CHUNK = 5000;
  for (let i = 0; i < prices.length; i += CHUNK) {
    const slice = prices.slice(i, i + CHUNK);
    appendFileSync(outPath, '  ' + slice.join(',\n  ') + ',\n', 'utf8');
  }

  appendFileSync(outPath, '];\n', 'utf8');
  console.log(`\nWritten to: ${outPath}`);
}

main().catch(err => {
  console.error('Error:', err);
  process.exit(1);
});
